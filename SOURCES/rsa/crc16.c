int crc16(char *buf, int len)
{
	int crc = 0;
	char *s = buf; 
	if (!len )
		return 0;

	while (len-- > 0)
		 crc = (crc >> 8) ^ table16[(crc ^ *s++) & 0xff];
//	sctually the code is
//		 crc = (crc >> 8) ^ (table16[(crc && 0xff) ^ (*s++ & 0xff)] & 0xff);
	return crc;
}
